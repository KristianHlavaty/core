#include	"libft.h"

int	ft_isalnum(int cn)
{
	if ((cn >= 'a' && cn <= 'z') || (cn >= 'A' && cn <= 'Z')
		|| (cn >= '0' && cn <= '9'))
		return (1);
	else
		return (0);
}
